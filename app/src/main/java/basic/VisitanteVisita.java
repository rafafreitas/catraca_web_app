public class VisitanteVisita {

    private int vivi_id;
    private String visita_id;
    private String visitante_id;
    private String visitante_visita_chegada;
    private String visitante_visita_saida;


    public VisitanteVisita() {
    }

    public int getVivi_id() {
        return vivi_id;
    }

    public void setVivi_id(int vivi_id) {
        this.vivi_id = vivi_id;
    }

    public String getVisita_id() {
        return visita_id;
    }

    public void setVisita_id(String visita_id) {
        this.visita_id = visita_id;
    }

    public String getVisitante_id() {
        return visitante_id;
    }

    public void setVisitante_id(String visitante_id) {
        this.visitante_id = visitante_id;
    }

    public String getVisitante_visita_chegada() {
        return visitante_visita_chegada;
    }

    public void setVisitante_visita_chegada(String visitante_visita_chegada) {
        this.visitante_visita_chegada = visitante_visita_chegada;
    }

    public String getVisitante_visita_saida() {
        return visitante_visita_saida;
    }

    public void setVisitante_visita_saida(String visitante_visita_saida) {
        this.visitante_visita_saida = visitante_visita_saida;
    }
}
